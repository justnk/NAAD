package com.streamliners.task0.Introduction;

public class SwapNos {
    public static void main(String[] args) {
        int a=10,b=5,temp;
        System.out.println("a = " + a + " and b = " + b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("a = " + a + " and b = " + b);
    }
}
